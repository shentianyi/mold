function MouldDetail() {
    var MouldDetail = document.getElementById("MouldDetail");
    //鼠标移入
    MouldDetail.onmouseover = function () {
        MouldDetail.style.backgroundColor = '#4F94CD';
    }
    //鼠标移出
    MouldDetail.onmouseout = function () {
        MouldDetail.style.backgroundColor = '#424A5D';
    }
}

